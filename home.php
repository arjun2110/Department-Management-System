      <h2>Department Vision and Mission</h2>
      <hr>
      <h4>Vision:</h4>
      <p>To emerge out as a prominent department offering a programme in its pursuit for academic excellence in order to develop professionally competent and socially responsible Information Technologists capable of meeting industry demands and social obligations in a vibrant global environment.</p>
      <hr>
      <h4>Mission:</h4>
      <p>To strive towards building an atmosphere that will be a catalyst for innovative ideas and learning. Providing IT students with various opportunities and experiences that can help them to thrive and prosper through a blend of academics, practical exposure and research programs. To pursue successful careers in a global environment.</p>
      <hr>
     <h4>HOD's Message</h4>
     <p>Keeping this objective in mind, young mindsets are educated here with full academic rigour to convert them into effective IT professionals. With the help of best of the infrastructure and the proper action plan, we train and educate engineers of outstanding ability who can become leaders in their profession.
The department regularly takes various initiatives; like Personality Development, Soft Skill sessions and employment oriented technical and management sessions for providing our students a clear and decisive edge in employ ability.
Department provides ample opportunities and tools to the students for grooming them as a productive and participating global citizens. We incorporate the good communication and technical skills as well as moral and ethical values for the overall development of the individual.
