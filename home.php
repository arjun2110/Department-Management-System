      <h2>Department of Information Technology</h2>
      <hr>
      
      <p>The Department was established in the year 2002 with an intake of 40 students.The current intake in B.E. (Information Technology) is 60 students.

Information Technology(IT) at MGMCET, Kamothe , is one of the Best IT Department in Engineering Institute in Navi Mumbai undertakes various unique approaches to educate the students for the emerging and highly challenging Industry requirements. The Department has various laboratories equipped with state-of-the-art computing facilities to support the research and teaching activities. The department has a Robotics Lab in collaboration with Indian Institute of Technology, Mumbai .

Pathway after Information Technology
IT Specialists are recruited in almost all ranges of employment such as finance, arts, medicine, defence, teaching and more. Apart from various opportunities available in IT firms, these professionals can get jobs in public sector and Government firms also. Moreover, a career in IT is considered as one of the most high-paying jobs and is full of opportunities as it is a never ending industry.</p>
